package cc.ccblog.entity;

public class GarbageClass {
	private	int garbageid;
	private String garbagename;
	private String garbageclass;
	
	
	public int getGarbageid() {
		return garbageid;
	}
	public void setGarbageid(int garbageid) {
		this.garbageid = garbageid;
	}
	public String getGarbagename() {
		return garbagename;
	}
	public void setGarbagename(String garbagename) {
		this.garbagename = garbagename;
	}
	public String getGarbageclass() {
		return garbageclass;
	}
	public void setGarbageclass(String garbageclass) {
		this.garbageclass = garbageclass;
	}
	
	
}
